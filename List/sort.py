#by sort usage it will sort the element in alphabetical oder
cars=['bmw', 'audi', 'toyota', 'farari']
cars.sort()
print(f"alphabetical oder:-\n{cars}")
# for reverse sort we need to use agument sort(reverse=True)
cars.sort(reverse=True)
print(f"revers alphabetical sorting:-\n{cars}")
